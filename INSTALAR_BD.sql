-- Ejecutar como usuario ICEBERG en Oracle

-- Tabla principal de solicitudes
CREATE TABLE ICEBERG.SOLICITUDES_PERMISOS (
    ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NIT_EMPLEADO        VARCHAR2(20)   NOT NULL,
    NIT_JEFE            VARCHAR2(20)   NOT NULL,
    NIT_RRHH            VARCHAR2(20),
    TIPO_SOLICITUD      VARCHAR2(60)   NOT NULL,
    FECHA_INICIO        DATE           NOT NULL,
    FECHA_FIN           DATE           NOT NULL,
    DURACION_HORAS      NUMBER(6,2),
    DURACION_DIAS       NUMBER(5,2),
    OBSERVACIONES       VARCHAR2(2000),
    RUTA_COMPROBANTE    VARCHAR2(500),
    ESTADO              VARCHAR2(30)   DEFAULT 'PENDIENTE_JEFE' NOT NULL,
    OBSERVACION_JEFE    VARCHAR2(2000),
    OBSERVACION_RRHH    VARCHAR2(2000),
    FECHA_GESTION_JEFE  DATE,
    FECHA_GESTION_RRHH  DATE,
    FECHA_CREACION      DATE           DEFAULT SYSDATE NOT NULL,
    FECHA_MODIFICACION  DATE,
    ACTIVO              NUMBER(1)      DEFAULT 1 NOT NULL
);

-- Tabla de notificaciones
CREATE TABLE ICEBERG.NOTIFICACIONES (
    ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NIT_DESTINATARIO    VARCHAR2(20)   NOT NULL,
    TIPO                VARCHAR2(30)   NOT NULL,  -- 'NUEVA_SOLICITUD', 'SOLICITUD_APROBADA', 'SOLICITUD_RECHAZADA', etc.
    MENSAJE             VARCHAR2(500)  NOT NULL,
    ID_SOLICITUD        NUMBER         NOT NULL,
    LEIDA               NUMBER(1)      DEFAULT 0 NOT NULL,  -- 0 = no leída, 1 = leída
    FECHA_CREACION      DATE           DEFAULT SYSDATE NOT NULL,
    FECHA_LECTURA       DATE
);

-- Índices para performance
CREATE INDEX idx_notif_destinatario ON ICEBERG.NOTIFICACIONES(NIT_DESTINATARIO);
CREATE INDEX idx_notif_leida ON ICEBERG.NOTIFICACIONES(LEIDA);
CREATE INDEX idx_notif_solicitud ON ICEBERG.NOTIFICACIONES(ID_SOLICITUD);
CREATE INDEX idx_solicitudes_empleado ON ICEBERG.SOLICITUDES_PERMISOS(NIT_EMPLEADO);
CREATE INDEX idx_solicitudes_jefe ON ICEBERG.SOLICITUDES_PERMISOS(NIT_JEFE);
CREATE INDEX idx_solicitudes_estado ON ICEBERG.SOLICITUDES_PERMISOS(ESTADO);
